class Operators
{
    public static void main(String[] args)
    {
        // Arithmetic operators


        //  Addiction 
        int num1 = 50;
        int num2 = 20;
        int add = num1 + num2;

        System.out.println(add);
        
        // Subtraction
        int Subtraction = num1 - num2;
        System.out.println(Subtraction);
        

        // Multiplication
        int Multiplication = num1 * num2;
        System.out.println(Multiplication);
        
        // Division
        int num3 = 50;
        int num4 = 5;
        int Division = num3 / num4;
        System.out.println(Division);


        // Increment
        num1++;                             //post increment
        int post_increment = num1;
        System.out.println(post_increment); 

        ++num1;                             //pre increment
        int pre_increment = num1;
        System.out.println(pre_increment);


        // Decrement

        num2--;                             //post decrement
        int post_decrement = num2;
        System.out.println(post_decrement);

        --num2;                             //pre decrement
        int pre_decrement = num2;
        System.out.println(pre_decrement);
       
    }

}



