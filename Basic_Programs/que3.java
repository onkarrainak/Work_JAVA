// 4.	Java Program to Check Whether a Number is Even or Odd
public class que3 {
    public static void main(String[] args) {
        int num = 22;
        String result;
        if (num%2==0)
           result = "Even";
        else 
            result = "Odd";

        System.out.println(num + "This number is " + result);
    }
}
