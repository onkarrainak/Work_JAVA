// 8.	Write a java program  to  print Table_of_given_Number

public class Table_of_given_Number {

    public static void main(String[] args) {
        int num = 10;
        int i;
        for (i=1;i<=10;i++){
            System.out.println(num +" * " + i + " =  " +
             num*i);
        }
    }
    
}
