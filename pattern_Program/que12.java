// Half Pyramid with Numbers

public class que12 {
    public static void main(String[] args) {
        
        int num =5;
        int i,j;

        for (i=1;i<=num;i++){
            for (j=1;j<=i;j++){
                System.out.print(j);
            }
            System.out.println();
        }

    }
}
